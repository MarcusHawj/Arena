inline const Glyph* find(char c) {
    if (c >= 'a' && c <= 'z') c = char(c - 'a' + 'A');
    for (const Glyph& g : GLYPHS)
        if (g.c == c) return &g;
    return nullptr;  // unknown chars (and spaces) render as gaps
}

// Pixel width of a string at a given scale (advance = 6 columns per char)
inline float text_width(const std::string& s, float scale) {
    return float(s.size()) * 6.0f * scale - scale;
}

